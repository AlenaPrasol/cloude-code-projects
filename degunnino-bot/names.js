function toCanonical(input) {
    if (!input) return '';
    const key = input.trim().toLowerCase()
        .replace(/ё/g,'е')
        .replace(/й/g,'и');
    // Точное совпадение (с ё)
    const exact = input.trim().toLowerCase();
    if (allForms[exact]) return allForms[exact];
    if (allForms[key])   return allForms[key];
    // Оставляем как есть с заглавной буквы
    return input.trim().charAt(0).toUpperCase() + input.trim().slice(1).toLowerCase();
}

function toGenitive(canonical) {
    if (!canonical) return '';
    const key = canonical.toLowerCase();
    if (specialGen[key]) return specialGen[key];

    const last2 = canonical.slice(-2).toLowerCase();
    const last1 = canonical.slice(-1).toLowerCase();

    // Женские окончания
    if (last2 === 'ия') return canonical.slice(0,-1) + 'и'; // Мария → Марии
    if (last2 === 'ья') return canonical.slice(0,-2) + 'ьи';
    if (last1 === 'а') {
        if (canonical.slice(-2,-1).toLowerCase() === 'ж' ||
            canonical.slice(-2,-1).toLowerCase() === 'ш' ||
            canonical.slice(-2,-1).toLowerCase() === 'ч') return canonical.slice(0,-1) + 'и';
        return canonical.slice(0,-1) + 'ы';
    }
    if (last1 === 'я') return canonical.slice(0,-1) + 'и';

    // Мужские окончания
    if (last2 === 'ий') return canonical.slice(0,-2) + 'ия'; // Сергий → Сергия
    if (last2 === 'ей') return canonical.slice(0,-2) + 'ея'; // Алексей → Алексея
    if (last2 === 'ай') return canonical.slice(0,-2) + 'ая'; // Николай → Николая
    if (last1 === 'ь')  return canonical.slice(0,-1) + 'я';
    if (last1 === 'й')  return canonical.slice(0,-1) + 'я';

    // Согласные
    const consonants = 'бвгджзклмнпрстфхцчшщ';
    if (consonants.includes(last1)) return canonical + 'а';

    return canonical;
}

